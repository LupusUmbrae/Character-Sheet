package org.moss.charactersheet.impl;

public class SpeedAndInitiative {

	int speed;
	int initiative;
	
	public SpeedAndInitiative(int speed, int initiative) {
		this.speed = speed;
		this.initiative = initiative;
	}
	
	public int getSpeed() {
		return speed;
	}
	
	public int getInitiative() {
		return initiative;
	}
}
